
public class Monstre {
	float force;
	float dexterite;
	float constitution;
	float intelligence;
	float sagesse;
	float charisme;
	public Monstre(float force, float dex, float cons, float intel, float sag, float chari) {
		this.force = force;
		this.dexterite = dex;
		this.constitution = cons;
		this.intelligence = intel;
		this.sagesse = sag;
		this.charisme = chari;
	}

}
